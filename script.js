function deal() {
  const cards = levels[document.querySelector("[name=level]:checked").id];
  document.querySelector('#cards').innerHTML = shuffle(cards).slice(0,10).map(cardTemplate).join('');
}
deal();

function cardTemplate ([kanji, hiragana, english]) {
  const content = `
    <p class="kanji">${kanji}</p>
    <p class="hiragana">${hiragana}</p>
    <p class="english">${english}</p>
  `;
  return `
    <input id="show-${kanji}" type="checkbox" />
    <label class="card hide" for="show-${kanji}">${content}</label>
    <input id="done-${kanji}" type="checkbox" />
    <label class="card" for="done-${kanji}">${content}</label>
    </label>`;
}

// https://stackoverflow.com/a/2450976/1253312
function shuffle(array) {
  var currentIndex = array.length, randomIndex;
  while (currentIndex) {
    randomIndex = Math.floor(Math.random() * currentIndex);
    currentIndex -= 1;
    [array[currentIndex], array[randomIndex]] = [array[randomIndex], array[currentIndex]];
  }
  return array;
}
